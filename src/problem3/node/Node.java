package problem3.node;

public class Node {
    private int data;
    private  Node next;
    //Constructor
    public Node(int data){
        this.data=data;
        next=null;
    }

    public int getData() {
        return data;
    }

    public void setData(int data) {
        this.data = data;
    }

    public Node getNext() {
        return next;
    }

    public void setNext(Node next) {
        this.next = next;
    }
}
